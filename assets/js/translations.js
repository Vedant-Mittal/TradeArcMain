// Language management class
class LanguageManager {
    constructor() {
        this.currentLanguage = this.getStoredLanguage() || 'en';
        this.supportedLanguages = ['en', 'hi', 'es', 'fr', 'de', 'ar'];
        this.init();
    }
    
    init() {
        this.setupLanguageToggle();
        this.translatePage();
        this.updateLanguageDisplay();
    }
    
    getStoredLanguage() {
        return localStorage.getItem('preferred-language');
    }
    
    setStoredLanguage(lang) {
        localStorage.setItem('preferred-language', lang);
    }
    
    setupLanguageToggle() {
        const languageToggle = document.querySelector('.language-toggle');
        const currentLangButton = document.querySelector('.current-language');
        const langOptions = document.querySelectorAll('.lang-option');
        
        if (!languageToggle || !currentLangButton) return;
        
        // Toggle dropdown visibility
        currentLangButton.addEventListener('click', (e) => {
            e.preventDefault();
            e.stopPropagation();
            languageToggle.classList.toggle('active');
        });
        
        // Close dropdown when clicking outside
        document.addEventListener('click', (e) => {
            if (!languageToggle.contains(e.target)) {
                languageToggle.classList.remove('active');
            }
        });
        
        // Handle language option clicks
        langOptions.forEach(option => {
            option.addEventListener('click', (e) => {
                e.preventDefault();
                const selectedLang = option.dataset.lang;
                this.changeLanguage(selectedLang);
                languageToggle.classList.remove('active');
            });
        });
        
        // Close with Escape key
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') {
                languageToggle.classList.remove('active');
            }
        });
    }
    
    changeLanguage(lang) {
        if (!this.supportedLanguages.includes(lang)) return;
        
        this.currentLanguage = lang;
        this.setStoredLanguage(lang);
        this.translatePage();
        this.updateLanguageDisplay();
        this.updateActiveLanguageOption();
        
        // Update document language attribute
        document.documentElement.lang = lang;
        
        // Show notification
        this.showLanguageNotification(lang);
    }
    
    translatePage() {
        const elements = document.querySelectorAll('[data-i18n]');
        const currentTranslations = translations[this.currentLanguage];
        
        if (!currentTranslations) return;
        
        elements.forEach(element => {
            const key = element.dataset.i18n;
            const translation = this.getNestedTranslation(currentTranslations, key);
            
            if (translation) {
                // Handle different element types
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    if (element.type === 'submit' || element.type === 'button') {
                        element.value = translation;
                    } else {
                        element.placeholder = translation;
                    }
                } else {
                    // Special handling for mobile menu toggle buttons to preserve arrows
                    if (element.classList.contains('mobile-menu-toggle-btn')) {
                        const chevron = element.querySelector('.mobile-menu-chevron');
                        const chevronHtml = chevron ? chevron.outerHTML : '<span class="mobile-menu-chevron">→</span>';
                        element.innerHTML = `<span>${translation}</span>${chevronHtml}`;
                    } else {
                        element.textContent = translation;
                    }
                }
            }
        });
        
        // Handle special cases for arrays (like benefits lists)
        this.translateLists();
        this.translateTestimonials();
    }
    
    translateLists() {
        const currentTranslations = translations[this.currentLanguage];
        if (!currentTranslations) return;
        
        // Translate custom sourcing benefits if present
        const benefitsList = document.querySelector('.benefits-list');
        if (benefitsList && currentTranslations.products?.custom?.benefits) {
            const benefitItems = benefitsList.querySelectorAll('li');
            benefitItems.forEach((item, index) => {
                if (currentTranslations.products.custom.benefits[index]) {
                    item.textContent = currentTranslations.products.custom.benefits[index];
                }
            });
        }
        
        // Translate quality certifications if present
        const certsList = document.querySelector('.certifications-list');
        if (certsList && currentTranslations.quality?.certifications) {
            const certItems = certsList.querySelectorAll('li');
            certItems.forEach((item, index) => {
                if (currentTranslations.quality.certifications[index]) {
                    item.textContent = currentTranslations.quality.certifications[index];
                }
            });
        }
    }
    
    translateTestimonials() {
        const currentTranslations = translations[this.currentLanguage];
        if (!currentTranslations?.testimonials?.items) return;
        
        const testimonialCards = document.querySelectorAll('.testimonial-card');
        testimonialCards.forEach((card, index) => {
            const testimonialData = currentTranslations.testimonials.items[index];
            if (testimonialData) {
                const textElement = card.querySelector('.testimonial-text');
                const authorElement = card.querySelector('.testimonial-author');
                const companyElement = card.querySelector('.testimonial-company');
                
                if (textElement) textElement.textContent = `"${testimonialData.text}"`;
                if (authorElement) authorElement.textContent = testimonialData.author;
                if (companyElement) companyElement.textContent = testimonialData.company;
            }
        });
    }
    
    getNestedTranslation(obj, key) {
        return key.split('.').reduce((o, k) => o && o[k], obj);
    }
    
    updateLanguageDisplay() {
        const currentLangButton = document.querySelector('.current-language');
        if (!currentLangButton) return;
        
        const flagIcon = currentLangButton.querySelector('.flag-icon');
        const langCode = currentLangButton.querySelector('.lang-code');
        
        const languageNames = {
            en: 'EN',
            hi: 'हिं',
            es: 'ES',
            fr: 'FR',
            de: 'DE',
            ar: 'ع'
        };
        
        if (flagIcon) {
            flagIcon.className = `flag-icon flag-${this.currentLanguage}`;
        }
        
        if (langCode) {
            langCode.textContent = languageNames[this.currentLanguage] || 'EN';
        }
    }
    
    updateActiveLanguageOption() {
        const langOptions = document.querySelectorAll('.lang-option');
        langOptions.forEach(option => {
            if (option.dataset.lang === this.currentLanguage) {
                option.classList.add('active');
            } else {
                option.classList.remove('active');
            }
        });
    }
    
    showLanguageNotification(lang) {
        const languageNames = {
            en: 'English',
            hi: 'हिन्दी',
            es: 'Español',
            fr: 'Français',
            de: 'Deutsch',
            ar: 'العربية'
        };
        
        const message = `Language changed to ${languageNames[lang]}`;
        
        // Use existing notification system if available
        if (typeof showNotification === 'function') {
            showNotification(message, 'success');
        }
    }
    
    // Method to get current language
    getCurrentLanguage() {
        return this.currentLanguage;
    }
    
    // Method to get translation for specific key
    translate(key) {
        const currentTranslations = translations[this.currentLanguage];
        return this.getNestedTranslation(currentTranslations, key) || key;
    }
}

// Export for use in other files
window.LanguageManager = LanguageManager;
window.translations = translations;